package Array;
public class _1_Array 
{
    public static void main(String args[])
    {
        //initialise array
        int nums[] = new int[4];
        nums [0] = 4;
        nums [1] = 2;
        nums [2] = 5;
        nums [3] = 9;

        //printing array
        for(int i =0; i<=3;i++)
        {
            System.out.println(nums[i]);
        }



    }    
}
